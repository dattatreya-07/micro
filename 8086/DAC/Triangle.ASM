	OUTPUT 2500AD
.ORG 2000H

DATA SEGMENT
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AL, 80H
    MOV DX, FFE6H
    OUT DX, AL

C1:
    MOV AL, 00H
UP:
    MOV DX, FFE0H
    OUT DX, AL
    INC AL
    CMP AL, FFH
    JNZ UP

DOWN:
    MOV DX, FFE0H
    OUT DX, AL
    DEC AL
    CMP AL, 00H
    JNZ DOWN

    JMP C1

CODE ENDS
END