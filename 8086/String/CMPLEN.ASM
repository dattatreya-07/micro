STR1 DB 'APPLE'
STR2 DB 'BANANA'
RES DB 00H
DATA ENDS
CODE SEGMENT
        ASSUME CS:CODE,DS:DATA
        MOV AX,DATA
        MOV DS,AX
L1: MOV AL,STR1
        MOV BL,STR2
        CMP AL,BL
        JE L4
        JB L3
        JA L2
L2: MOV RES,01H
        JMP L5
L3: MOV RES,FFH
    JMP L5
L4: MOV RES,00H
        JMP L5
L5: HLT
CODE ENDS
END

