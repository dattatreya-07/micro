	OUTPUT 2500AD
.ORG 2000H
DATA  SEGMENT
NUM1 DB 04H
NUM2 DB 02H
RES DB 00H
DATA ENDS
CODE SEGMENT
	ASSUME  CS:CODE,DS:DATA
	MOV AX,DATA
	MOV DS,AX
	MOV AL,NUM1
    MUL AL
    MOV BX,AX
	MOV AL,NUM2
    MUL AL
	ADD BX,AX
	MOV AL,NUM1
	ADD AL,NUM2
	MOV CL,AL
	MOV AX,BX
	MOV AH,00H
	DIV CL
	MOV RES,AL
CODE ENDS
END


